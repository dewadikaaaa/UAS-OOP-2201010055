    private void btncariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncariActionPerformed
        // TODO add your handling code here:
        // Ambil teks pencarian dari JTextField txtcari
    String searchTerm = txtcari.getText();
    
    try {
        String sql = "SELECT * FROM penyewaann WHERE nama LIKE ?";
        java.sql.Connection conn = koneksi.configDB();
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, "%" + searchTerm + "%");

        ResultSet rs = pst.executeQuery();

        // Clear existing rows in the table model
        DefaultTableModel tableModel = (DefaultTableModel) tabel_data.getModel();
        tableModel.setRowCount(0); // Menghapus semua baris yang ada

        while (rs.next()) {
            String id = rs.getString("id");
            String nama = rs.getString("nama");
            String jenisKelamin = rs.getString("jenis_kelamin");
            String alamat = rs.getString("alamat");
            String ht = rs.getString("ht");
            String tambahan = rs.getString("tambahan");
            Date tglSewa = rs.getDate("tgl_sewa");
            Date tglKembali = rs.getDate("tgl_kembali");
            String pembayaran = rs.getString("pembayaran");
            int totalHari = rs.getInt("total_hari");

            // Create row object array
            Object[] row = {id, nama, jenisKelamin, alamat, ht, tambahan, tglSewa, tglKembali, pembayaran, totalHari};

            // Add row to table model
            tableModel.addRow(row);
        }

        pst.close();
        conn.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
    }//GEN-LAST:event_btncariActionPerformed

