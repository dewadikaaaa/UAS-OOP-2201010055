    private void tabel_dataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_dataMouseClicked
        // TODO add your handling code here:
        
        int rowIndex = tabel_data.getSelectedRow();
        // Mengambil nilai dari kolom yang sesuai dengan baris yang diklik
        String id = tabel_data.getValueAt(rowIndex, 0).toString();
        String nama = tabel_data.getValueAt(rowIndex, 1).toString();
        String jenisKelamin = tabel_data.getValueAt(rowIndex, 2).toString();
        String alamat = tabel_data.getValueAt(rowIndex, 3).toString();
        String ht = tabel_data.getValueAt(rowIndex, 4).toString();
        String tambahan = tabel_data.getValueAt(rowIndex, 5).toString();
        String tglSewa = tabel_data.getValueAt(rowIndex, 6).toString();
        String tglKembali = tabel_data.getValueAt(rowIndex, 7).toString();
        String pembayaran = tabel_data.getValueAt(rowIndex, 8).toString();
        String totalHari = tabel_data.getValueAt(rowIndex, 9).toString();

        // Menampilkan nilai dari kolom yang sesuai ke dalam elemen input
        txtid.setText(id);
        txtnama.setText(nama);
        txtalamat.setText(alamat);

        if (jenisKelamin.equals("Laki-laki")) {
            rblaki.setSelected(true);
        } else if (jenisKelamin.equals("Perempuan")) {
            rbperempuan.setSelected(true);
        } else {
            // Reset radio buttons if gender is not recognized
            buttonGroupJenisKelamin.clearSelection();
        }

        // Menetapkan nilai pada list ht
        listht.setSelectedValue(ht, true);

        // Menetapkan nilai pada checkboxes tambahan
        cbbaterai.setSelected(tambahan.contains("Baterai"));
        cbtripod.setSelected(tambahan.contains("Tripod"));
        cbsdcard.setSelected(tambahan.contains("SD Card"));

        // Menetapkan nilai pada JDateChooser
        try {
            java.util.Date sewaDate = new SimpleDateFormat("yyyy-MM-dd").parse(tglSewa);
            datesewa.setDate(sewaDate);
            java.util.Date kembaliDate = new SimpleDateFormat("yyyy-MM-dd").parse(tglKembali);
            datekembali.setDate(kembaliDate);
        } catch (ParseException e) {
            e.printStackTrace();
        }

        // Menetapkan nilai pada combobox pembayaran
        combobayar.setSelectedItem(pembayaran);

        txttotalhari.setText(totalHari);
                
        
        
    }//GEN-LAST:event_tabel_dataMouseClicked

