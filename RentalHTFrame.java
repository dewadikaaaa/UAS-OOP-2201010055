    private void load_tabel() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("Nama");
    model.addColumn("Jenis Kelamin");
    model.addColumn("Alamat");
    model.addColumn("Ht");
    model.addColumn("Tambahan");
    model.addColumn("Tanggal Sewa");
    model.addColumn("Tanggal Kembali");
    model.addColumn("Pembayaran");
    model.addColumn("Total Hari");

    // Menampilkan data database ke dalam tabel
    try {
        int id = 1;
        String sql = "SELECT * FROM penyewaann";
        java.sql.Connection conn = koneksi.configDB();
        java.sql.Statement stm = conn.createStatement();
        java.sql.ResultSet res = stm.executeQuery(sql);
        
        while (res.next()) {
            model.addRow(new Object[]{res.getString(1), res.getString(2), res.getString(3), res.getString(4), res.getString(5), res.getString(6), res.getString(7), res.getString(8), res.getString(9), res.getString(10)});
        }
        
        tabel_data.setModel(model);
    } catch (SQLException e) {
        e.printStackTrace();
    }
    }
    
